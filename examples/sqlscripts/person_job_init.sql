-- Job Type Package

CREATE OR REPLACE PACKAGE JOB_TYPE_PKG AS
  -- Get all the Job Types as a sys_refcursor
  PROCEDURE GET(PCURSOR OUT SYS_REFCURSOR);
  
  -- Get a Job Type by name. <-- Not very useful atm
  PROCEDURE GET(PNAME IN JOB_TYPE.NAME%TYPE, PCURSOR OUT SYS_REFCURSOR);
  
  -- Create a new JobType
  PROCEDURE NEW(PNAME IN JOB_TYPE.NAME%TYPE);
END JOB_TYPE_PKG;
/

CREATE OR REPLACE PACKAGE BODY JOB_TYPE_PKG AS
  PROCEDURE GET(PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT NAME FROM JOB_TYPE;
  END GET;
  
  PROCEDURE GET(PNAME IN JOB_TYPE.NAME%TYPE, PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT NAME
      FROM JOB_TYPE
      WHERE NAME=UPPER(PNAME);
  END GET;
  
  PROCEDURE NEW(PNAME IN JOB_TYPE.NAME%TYPE)
  IS
  BEGIN
    INSERT INTO JOB_TYPE(NAME)
    VALUES(UPPER(PNAME));
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END NEW;

END JOB_TYPE_PKG;
/


