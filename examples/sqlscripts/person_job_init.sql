-- Person Package

CREATE OR REPLACE PACKAGE PERSON_PKG AS
  -- Gets a person by id from the person view
  PROCEDURE GET(PID IN PERSON.ID%TYPE, PCURSOR OUT SYS_REFCURSOR);
  
  -- Gets a person by first name and last name from the person view. The first name or last name can be null
  PROCEDURE GETBYNAME(PFIRST IN PERSON.FIRST_NAME%TYPE, PLAST IN PERSON.LAST_NAME%TYPE, PCURSOR OUT SYS_REFCURSOR);
  
  -- Update a person
  PROCEDURE UPDATEPERSON(PID IN INTEGER, PJOB_ID IN INTEGER, PFIRST IN PERSON.FIRST_NAME%TYPE, PLAST IN PERSON.LAST_NAME%TYPE, PSAL IN DECIMAL);

  -- Creates a new person
  PROCEDURE NEW( PJOB_ID IN INTEGER
               , PFIRST IN PERSON.FIRST_NAME%TYPE
               , PLAST IN PERSON.LAST_NAME%TYPE
               , PSAL IN PERSON.SALARY%TYPE
               , PID IN INTEGER DEFAULT SEQ_PERSON_ID.NEXTVAL);
  -- Deletes a person
  PROCEDURE DELETE(PID IN INTEGER);
END PERSON_PKG;
/

CREATE OR REPLACE PACKAGE BODY PERSON_PKG AS
  -- Gets a person by id
  PROCEDURE GET(PID IN PERSON.ID%TYPE, PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT *
      FROM PERSON_VIEW
      WHERE ID=PID;
  END GET;
  
  -- Gets a person by first name and last name. The first name or last name can be null
  PROCEDURE GETBYNAME(PFIRST IN PERSON.FIRST_NAME%TYPE, PLAST IN PERSON.LAST_NAME%TYPE, PCURSOR OUT SYS_REFCURSOR)
  IS
    UFIRST VARCHAR2(128);
    ULAST VARCHAR2(128);
  BEGIN
    UFIRST := UPPER(PFIRST);
    ULAST := UPPER(PLAST);
    IF PFIRST!=NULL AND PLAST!=NULL THEN
      OPEN PCURSOR FOR
        SELECT * FROM PERSON WHERE FIRST_NAME=UFIRST AND LAST_NAME=ULAST;
    ELSE
      OPEN PCURSOR FOR
        SELECT * FROM PERSON WHERE LAST_NAME=ULAST OR FIRST_NAME=UFIRST;
    END IF;
  END GETBYNAME;
  
  -- Update a person
  PROCEDURE UPDATEPERSON(PID IN INTEGER, PJOB_ID IN INTEGER, PFIRST IN PERSON.FIRST_NAME%TYPE, PLAST IN PERSON.LAST_NAME%TYPE, PSAL IN DECIMAL)
  IS
  BEGIN
    UPDATE PERSON
    SET FIRST_NAME=UPPER(PFIRST)
      , LAST_NAME=UPPER(PLAST)
      , JOB_ID=PJOB_ID
      , SALARY=PSAL
    WHERE ID=PID;
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END UPDATEPERSON;
  
  -- Creates a new person
  PROCEDURE NEW( PJOB_ID IN INTEGER
               , PFIRST IN PERSON.FIRST_NAME%TYPE
               , PLAST IN PERSON.LAST_NAME%TYPE
               , PSAL IN PERSON.SALARY%TYPE
               , PID IN INTEGER DEFAULT SEQ_PERSON_ID.NEXTVAL)
  IS
  BEGIN
    INSERT INTO PERSON(ID, JOB_ID, FIRST_NAME, LAST_NAME, SALARY)
    VALUES(PID, PJOB_ID, UPPER(PFIRST), UPPER(PLAST), PSAL);
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END NEW;
  
  -- Deletes a person
  PROCEDURE DELETE(PID IN INTEGER)
  IS
  BEGIN
    DELETE FROM PERSON WHERE ID=PID;
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END DELETE;
  
END PERSON_PKG;
/

