-------------------------------------------------------------------------------
-- Tables
-------------------------------------------------------------------------------
DROP SEQUENCE SEQ_JOB_ID;
DROP SEQUENCE SEQ_PERSON_ID;
DROP TABLE PERSON;
DROP TABLE JOB;
DROP TABLE JOB_TYPE;
CREATE TABLE JOB_TYPE
( NAME VARCHAR2(128)
, CONSTRAINT PK_JOB_TYPE_NAME PRIMARY KEY(NAME)
);

CREATE TABLE JOB
( ID INTEGER
, TYPE VARCHAR2(128)
, NAME VARCHAR2(128)
, DESCRIPTION VARCHAR2(128)
, CONSTRAINT PK_JOB_ID PRIMARY KEY(ID)
, CONSTRAINT FK_JOB_TYPE FOREIGN KEY (TYPE) REFERENCES JOB_TYPE(NAME)
);

CREATE TABLE PERSON
( ID INTEGER
, JOB_ID INTEGER
, FIRST_NAME VARCHAR2(128)
, LAST_NAME VARCHAR2(128)
, SALARY DECIMAL(12,2)
, CONSTRAINT PK_PERSON_ID PRIMARY KEY(ID)
, CONSTRAINT FK_JOB_ID FOREIGN KEY (JOB_ID) REFERENCES JOB(ID)
);


-------------------------------------------------------------------------------
-- Create Views
-------------------------------------------------------------------------------
CREATE OR REPLACE VIEW PERSON_VIEW
AS
  SELECT P.ID,
         LAST_NAME||', '||FIRST_NAME PERSON_NAME,
         FIRST_NAME,
         LAST_NAME,
         SALARY PERSON_SALARY, 
         TYPE, 
         J.NAME JOB_NAME, 
         DESCRIPTION JOB_DESCRIPTION
  FROM PERSON P
  INNER JOIN JOB J ON P.JOB_ID = J.ID
  ORDER BY LAST_NAME, FIRST_NAME
WITH READ ONLY;


