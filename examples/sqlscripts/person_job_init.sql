-- Job Package


CREATE OR REPLACE PACKAGE JOB_PKG AS
  -- Get a job by id
  PROCEDURE GET(PID IN INTEGER, PCURSOR OUT SYS_REFCURSOR);
  
  -- Get a job by name
  PROCEDURE GET(PNAME IN JOB.NAME%TYPE, PCURSOR OUT SYS_REFCURSOR);
  
  -- Get all the jobs
  PROCEDURE GET(PCURSOR OUT SYS_REFCURSOR);
  
  -- Create a new job
  PROCEDURE NEW(PTYPE IN JOB.TYPE%TYPE, PNAME IN JOB.NAME%TYPE, PDESC IN JOB.DESCRIPTION%TYPE, PID IN JOB.ID%TYPE DEFAULT SEQ_JOB_ID.NEXTVAL);
  
  -- Update a job by id
  PROCEDURE UPDATEJOB(PID IN JOB.ID%TYPE, PTYPE IN JOB.TYPE%TYPE, PNAME IN JOB.NAME%TYPE, PDESC IN JOB.DESCRIPTION%TYPE);
  
  -- Deletes a job by id
  PROCEDURE DELETE(PID IN JOB.ID%TYPE);
  
END JOB_PKG;
/

CREATE OR REPLACE PACKAGE BODY JOB_PKG AS
  PROCEDURE GET(PID IN INTEGER, PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT *
      FROM JOB
      WHERE ID=PID;
  END GET;
  
  PROCEDURE GET(PNAME IN JOB.NAME%TYPE, PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT *
      FROM JOB
      WHERE NAME=UPPER(PNAME);
  END GET;
  
  PROCEDURE GET(PCURSOR OUT SYS_REFCURSOR)
  IS
  BEGIN
    OPEN PCURSOR FOR
      SELECT *
      FROM JOB;
  END GET;
  
  PROCEDURE NEW(PTYPE IN JOB.TYPE%TYPE, PNAME IN JOB.NAME%TYPE, PDESC IN JOB.DESCRIPTION%TYPE, PID IN JOB.ID%TYPE DEFAULT SEQ_JOB_ID.NEXTVAL)
  IS
  BEGIN
    INSERT INTO JOB(ID, TYPE, NAME, DESCRIPTION)
    VALUES(PID,UPPER(PTYPE),UPPER(PNAME),PDESC);
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END NEW;
  
  PROCEDURE UPDATEJOB(PID IN JOB.ID%TYPE, PTYPE IN JOB.TYPE%TYPE, PNAME IN JOB.NAME%TYPE, PDESC IN JOB.DESCRIPTION%TYPE)
  IS
  BEGIN
    UPDATE JOB
    SET TYPE=UPPER(PTYPE)
      , NAME=UPPER(PNAME)
      , DESCRIPTION=UPPER(PDESC)
    WHERE ID=PID;
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END UPDATEJOB;
  
  PROCEDURE DELETE(PID IN JOB.ID%TYPE)
  IS
  BEGIN
    DELETE FROM JOB WHERE ID=PID;
    COMMIT;
    
    EXCEPTION
      WHEN OTHERS THEN
        ROLLBACK;
        RAISE;
  END DELETE;
END JOB_PKG;
